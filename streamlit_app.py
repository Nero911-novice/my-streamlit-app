            ax.axhline(best_mean_test2, color='orange', linestyle='--', alpha=0.5, 
                      label=f"–°—Ä–µ–¥–Ω–µ–µ –¢–µ—Å—Ç 2 (–ª—É—á—à–∏–µ): {best_mean_test2:.1f}")
            
            # –ù–∞—Å—Ç—Ä–æ–π–∫–∞ –≥—Ä–∞—Ñ–∏–∫–∞
            ax.set_title("–†–µ–≥—Ä–µ—Å—Å–∏—è –∫ —Å—Ä–µ–¥–Ω–µ–º—É")
